    private void kelimeResimHazirla() {
        try {
            acilanharf = 0;
            harfler = new ArrayList<Character>();
            verilenharfler = new ArrayList<Character>();
            temizle();
            String cizgi = "";
            Kontrol kntr = new Kontrol();
            ArrayList<ImageIcon> imageList = new ArrayList<ImageIcon>();
            ArrayList<JLabel> rsmler = new ArrayList<JLabel>();
            rsmler.add(rsm1);
            rsmler.add(rsm2);
            rsmler.add(rsm3);
            rsmler.add(rsm4);

            kelime = dao.KelimeDAO.rastgeleKelimeGetir();
            ArrayList<Resim> resimler = dao.ResimDAO.kelimeResimleriniGetir(kelime);
            imageList = kntr.blobToImageList(resimler);
            for (int i = 0; i < imageList.size(); i++) {
                rsmler.get(i).setIcon(imageList.get(i));
            }

            for (int s = 0; s < kelime.getIcerik().length(); s++) {
                char a = kelime.getIcerik().charAt(s);
                harfler.add(a);
                cizgi += "_ ";
                verilenharfler.add('_');
            }
            lblKelime.setText(cizgi);
        } catch (Exception ex) {
            System.out.print("Resim hata-kelime" + ex);
        }
    }
