    private void btnGonderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGonderActionPerformed
        Kontrol kntr = new Kontrol();
        if (kntr.kelimeKontrol(txtKelime.getText(), kelime)) {
            puanKontrol("dogru");
            kelimeResimHazirla();
        } else {
            puanKontrol("yanlis");
            lblSonuc.setText("Tahmininiz Yanlış");
        }
    }//GEN-LAST:event_btnGonderActionPerformed

    private void btnHarfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHarfActionPerformed

        if (puan >= 10 && (kelime.getIcerik().length() - acilanharf) > 1) {
            acilanharf += 1;
            puanKontrol("harf");
            String kelime = "";
            Random rnd = new Random();
            int harfindex = rnd.nextInt(harfler.size());
            char harf = harfler.get(harfindex);
            while (verilenharfler.indexOf(harf) == harfindex) {
                harfindex = rnd.nextInt(harfler.size());
                harf = harfler.get(harfindex);
            }
            verilenharfler.set(harfindex, harf);
            for (int i = 0; i < verilenharfler.size(); i++) {
                kelime += verilenharfler.get(i) + " ";
            }
            lblKelime.setText(kelime);
        } else {
            showMessageDialog(null, "Kelimeye ait harf açma sınırını doldurdunuz , veya yeterli puanınız yok...");
          
        }
    }//GEN-LAST:event_btnHarfActionPerformed

