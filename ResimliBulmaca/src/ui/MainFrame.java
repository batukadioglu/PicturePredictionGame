    public void puanKontrol(String durum) {
        switch (durum) {
            case "dogru":
                puan += (kelime.getIcerik().length() - acilanharf) * 3;
                break;
            case "yanlis":
                puan += -3;
                break;
            case "harf":
                puan += -10;
                break;
            case "pas":
                puan += -5;
                break;
        }
        if (puan < 0) {
            lblPuan.setText(Integer.toString(puan));
            showMessageDialog(null, "- puana düştünüz oyun baştan başlıyor...");
            temizle();
            kelimeResimHazirla();
            puan = 50;
        }
        lblPuan.setText(Integer.toString(puan));
    }

